        "def train(model, dataloader, epoch, optimizer, criterion):\n",
        "         \n",
        "      model.train()\n",
        "      train_loss = []\n",
        "      train_acc = []\n",
        "      for batch_idx, (data, target) in enumerate(dataloader):\n",
